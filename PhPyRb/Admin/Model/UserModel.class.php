	namespace Admin\Controller;
	use Think\Model;
	
	class UserModel extends Model{
		protected $User;
		function _initialize(){
			$this->User = M('User');
		}
		
		function login($_where = array()){
			$userinfo = $this->User->where($_where)->find();
			return $userinfo;
		}
	}	